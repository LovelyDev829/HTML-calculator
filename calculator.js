let chk="0";
var res="0";
var p_btn="no";
var val="0";
var chk_del="0";
var chk_dot_hou="0";

function calc(){
    if(p_btn=="no"){
        res=document.getElementById("screen_id").value;
    }
    else{
        var ttt=document.getElementById("screen_id").value;
        var a=Number(res);
        var b=Number(ttt);
        if(p_btn=="plus")          var c=a+b;
        else if(p_btn=="minus")    var c=a-b;
        else if(p_btn=="multiple") var c=a*b;
        else if(p_btn=="divide")   var c=a/b;
        res=c; 
        p_btn="no";
        document.getElementById("screen_id").value=res;
    }
    chk_del="1";
}

